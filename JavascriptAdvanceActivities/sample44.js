const person = {
  firstName: "John",
  lastName: "Doe",
  age: 30,
};

 

// Extract values from the 'person' object and assign them to variables
const { firstName, lastName, age } = person;

 

console.log(firstName); // Outputs: "John"
console.log(lastName);  // Outputs: "Doe"
console.log(age);       // Outputs: 30